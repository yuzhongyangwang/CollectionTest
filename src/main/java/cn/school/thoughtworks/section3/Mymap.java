package cn.school.thoughtworks.section3;

public class Mymap {
    String k;
    int v;
    public Mymap() {

    }
    public Mymap(String k, int v) {
        this.k = k;
        this.v = v;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public int getV() {
        return v;
    }

    public void setV(int v) {
        this.v = v;
    }
}
